export function getStockById(id: number): stock | undefined {
  let left = 0;
  let right = stocks.length - 1;

  while (left <= right) {
    const mid = Math.floor((left + right) / 2);
    const midStock = stocks[mid];

    if (midStock.id === id) {
      return midStock;
    }

    if (midStock.id < id) {
      left = mid + 1;
    } else {
      right = mid - 1;
    }
  }

  return undefined;
}
